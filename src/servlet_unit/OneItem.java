package servlet_unit;

public class OneItem {
	String itm;
	String con;
	boolean cas, onl ,exp, tot;
	public String getItm() {
		return itm;
	}
	public void setItm(String itm) {
		this.itm = itm;
	}
	public String getCon() {
		return con;
	}
	public void setCon(String con) {
		this.con = con;
	}
	public boolean isCas() {
		return cas;
	}
	public void setCas(boolean cas) {
		this.cas = cas;
	}
	public boolean isOnl() {
		return onl;
	}
	public void setOnl(boolean onl) {
		this.onl = onl;
	}
	public boolean isExp() {
		return exp;
	}
	public void setExp(boolean exp) {
		this.exp = exp;
	}
	public boolean isTot() {
		return tot;
	}
	public void setTot(boolean tot) {
		this.tot = tot;
	}
	
}
